package com.example.order.domain;

public class Owner {
	@org.springframework.data.annotation.Id
	private Integer Id;
	private String name;

	public Integer getId() {
		return Id;
	}

	public void setId(Integer id) {
		Id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
